import SwiftUI

extension Font {
    static func rubikRegular(size: CGFloat) -> Font {
        Font.custom("RubikOne-Regular", size: size)
    }
    static func montserratRegular(size: CGFloat) -> Font {
        Font.custom("Montserrat-Regular", size: size)
    }
    static func montserratBold(size: CGFloat) -> Font {
        Font.custom("Montserrat-Bold", size: size)
    }
    static func montserratMedium(size: CGFloat) -> Font {
        Font.custom("Montserrat-Medium", size: size)
    }
    static func montserratSemiBold(size: CGFloat) -> Font {
        Font.custom("Montserrat-SemiBold", size: size)
    }
    static func ptSansRegular(size: CGFloat) -> Font {
        Font.custom("PTSans-Regular", size: size)
    }
    static func ptSansBold(size: CGFloat) -> Font {
        Font.custom("PTSans-Bold", size: size)
    }
    static func ptSansBoldItalic(size: CGFloat) -> Font {
        Font.custom("PTSans-BoldItalic", size: size)
    }
    static func ptSansItalic(size: CGFloat) -> Font {
        Font.custom("PTSans-Italic", size: size)
    }
}
